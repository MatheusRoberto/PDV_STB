    private void btnEnviarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnviarActionPerformed
        Cliente cli = new Cliente();
        cli.setNome(txtnome.getText());
        cli.setRazaoSocial(txtrazaosocial.getText());
        cli.setCnpjCpf(txtcnpj.getText());
        cli.setTelefone(txttelefone.getText());
        cli.setEmail(txtemail.getText());
        Endereco ender = new Endereco();
        ender.setCep(txtcep.getText());
        ender.setCidade(txtcidade.getText());
        ender.setLogradouro(txtlogradouro.getText());
        ender.setEstado(cbEstado.getSelectedItem().toString());
        ender.setBairro(txtbairro.getText());
        ender.setComplemento(txtcomplemento.getText());
        ender.setNumero(txtnumero.getText());

      String codigo = txtcod.getText();
        if (!codigo.isEmpty()){
            cli.setIdCliente(Integer.valueOf(codigo)); 
            
        EnderecoDAO edao = new EnderecoDAO();
        edao.salvar(ender);

        cli.setEndereco(ender);

        ClienteDAO cdao = new ClienteDAO();
        cdao.salvar(cli);

        txtnome.setEnabled(false);
        txtcnpj.setEnabled(false);
        txtcpf.setEnabled(false);
        txtemail.setEnabled(false);
        txttelefone.setEnabled(false);
        txtcod.setEditable(false);
        txtrazaosocial.setEnabled(false);

        btneditar.setEnabled(false);
        btnEnviar.setEnabled(false);

        txtnome.setText("");
        txtcnpj.setText("");
        txtcpf.setText("");
        txtemail.setText("");
        txttelefone.setText("");
        txtcod.setText("");
        txtrazaosocial.setText("");
        txtnome.requestFocus();

        JOptionPane.showMessageDialog(null, "Enviado com Sucesso");
        
        
        }

    }//GEN-LAST:event_btnEnviarActionPerformed

