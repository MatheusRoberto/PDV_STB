    private void btnbuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnbuscarActionPerformed
        String texto = txtbuscar.getText();
        ClienteDAO cdao = new ClienteDAO();
        vetorcliente = cdao.buscacliente(texto);
        DefaultListModel modelo = new DefaultListModel();
        for (Cliente cliente : vetorcliente){
            modelo.addElement(cliente.getNome());
            modelo.addElement(cliente.getCnpjCpf());
               modelo.addElement(cliente.getEmail());
               modelo.addElement(cliente.getRazaoSocial());
           listbuscar.setModel(modelo);
            listbuscar.repaint();
        }

    }//GEN-LAST:event_btnbuscarActionPerformed

